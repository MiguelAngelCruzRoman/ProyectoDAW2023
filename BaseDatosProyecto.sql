create database consultorioBD;

use consultorioBD;



create table medico(
	id int not null auto_increment primary key,
    especialidad varchar(50) not null,
    turno varchar(50) not null,
    diasLaborales varchar(50) not null,
    created_at datetime,
    updated_at datetime null,
    deleted_at datetime null
);



create table paciente(
	id int not null auto_increment,
    primary key (id),
    CURP varchar(18) not null ,
    statusSeguro varchar(50) not null,
    tipoSangre varchar(3) not null,
    alergia varchar(250) null,
    fechaRevision date null,
    motivoRevision text null,
    habitoToxico varchar(250) null,
    condicionesPrevias varchar(250) null,
	created_at datetime,
    updated_at datetime null,
    deleted_at datetime null
);



create table medico_paciente(
	id int not null auto_increment primary key,
    medico int  not null,
    constraint id_medicoConculta foreign key(medico) references medico(id),
    paciente int not null,
    constraint id_pacienteConsulta foreign key(paciente) references paciente(id),
    created_at datetime,
    updated_at datetime null,
    deleted_at datetime null
);



create table users(
	id int not null auto_increment,
    username varchar(50),
    correo varchar(100) not null,
    password varchar(150),
    status boolean,
	created_at datetime,
	updated_at datetime null,
    deleted_at datetime null,
    medico int null,
    constraint id_medico foreign key(medico) references medico(id),
    paciente int null,
    constraint id_paciente foreign key(paciente) references paciente(id),
    primary key(id)
);



create table  userinfo (
	id int not null,
    primerNombre varchar(50) not null,
	segundoNombre varchar(50) null default '',
    apellidoPaterno varchar(50) not null,
    apellidoMaterno varchar(50) not null,
    genero varchar(1) not null,
    telefono varchar(15) not null,
    foto varchar(250) not null,
    status boolean default true,
	created_at datetime,
	updated_at datetime null,
    deleted_at datetime null,
    constraint id_users foreign key(id) references users(id),
    primary key(id)
);



create table direccion(
	id int not null auto_increment,
    primary key (id),
    userinfo int not null,
	constraint id_userinfo foreign key(userinfo) references userinfo(id),
    tipo varchar(50) not null,
	estado varchar(50) not null,
    municipio varchar(50) not null,
    colonia varchar(50) not null,
    calle varchar(50) not null,
    CP varchar(5) not null,
    noExt int not null,
    noInt int null,
    created_at datetime,
	updated_at datetime null,
    deleted_at datetime null
);



create table medicamentos(
	id int not null auto_increment,
    primary key(id),
    nombreComercial varchar(50) not null,
    nombreCinetifico varchar(80) not null,
    formaFarmaceutica varchar(20) not null,
    dosis int not null,
    fechaCaducidad date not null,
    loteFabricacion varchar(10) not null,
    version varchar(15) not null,
    simbolo varchar(250) null,
    imagenEmpaque varchar(150) not null,
    stock int DEFAULT 50,
    created_at datetime,
	updated_at datetime null,
    deleted_at datetime null
);


create table consultas(
	id int not null auto_increment primary key,
    lugar text not null,
    hora time not null,
    fecha date not null,
    motivo text not null,
    medico_paciente int not null,
    constraint id_medico_paciente foreign key(medico_paciente) references medico_paciente(id),
    created_at datetime,
	updated_at datetime null,
    deleted_at datetime null
);



create table receta(
	id int not null primary key auto_increment,
    consulta int not null,
    constraint id_consulta foreign key(consulta) references consultas(id),
    fechaVencimiento date not null,
    status boolean DEFAULT TRUE,
	created_at datetime,
	updated_at datetime null,
    deleted_at datetime null
);

create table receta_medicamento(
	id int not null primary key auto_increment,
	medicamento int not null,
	constraint id_medicamento foreign key(medicamento) references medicamentos(id),
    receta int not null,
	constraint id_receta foreign key(receta) references receta(id),
    created_at datetime default now(),
	updated_at datetime null,
    deleted_at datetime null
);


